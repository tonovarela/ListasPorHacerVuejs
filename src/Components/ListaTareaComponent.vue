<template>
<ul class="list-group">
    <li class="list-group-item" v-for="(tarea,indice) of tareas" v-bind:class="{terminada:tarea.terminar}">
        {{tarea.texto}}
        <span class="pull-right">
            <button class="btn btn-success btn-xs glyphicon glyphicon-ok" v-on:click="tarea.terminar=!tarea.terminar">
            </button>
            <button class="btn btn-danger btn-xs glyphicon glyphicon-remove" @click="borrar(indice)" ></button>
        </span>

    </li>
</ul>
</template>

<script>
import {bus} from './../main.js';
export default {
    props: ['tareas'],
    data() {
        return {}
    },
    methods: {
        borrar: function (indice) {
            this.tareas.splice(indice, 1);
            bus.actualizarContador(this.tareas.length);
        
        }

    }
}
</script>

<style scoped>
.terminada {
    color: gray;
    text-decoration: line-through;
}
</style>
